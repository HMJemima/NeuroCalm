from __future__ import annotations

import copy
import importlib.util
import os
from pprint import pformat
from typing import Iterable

from app.config import get_settings

settings = get_settings()


def resolve_cache_path(path: str | None = None) -> str:
    cache_path = path or settings.PREDICTION_CACHE_PATH
    if os.path.isabs(cache_path):
        return cache_path
    return os.path.abspath(cache_path)


def normalize_cache_key(value: str) -> str:
    return value.replace("\\", "/").strip().lower()


def build_cache_keys(
    file_path: str,
    *,
    original_filename: str | None = None,
    source_dir: str | None = None,
) -> list[str]:
    basename = original_filename or os.path.basename(file_path)
    return [normalize_cache_key(basename)] if basename else []


def load_prediction_cache(path: str | None = None) -> dict[str, dict]:
    cache_path = resolve_cache_path(path)
    if not os.path.exists(cache_path):
        return {}

    module_name = f"prediction_cache_{abs(hash(cache_path))}"
    spec = importlib.util.spec_from_file_location(module_name, cache_path)
    if spec is None or spec.loader is None:
        return {}

    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    cache = getattr(module, "PREDICTION_CACHE", {})
    return cache if isinstance(cache, dict) else {}


def find_cached_prediction(
    file_path: str,
    *,
    cache_keys: Iterable[str] | None = None,
    original_filename: str | None = None,
) -> dict | None:
    cache = load_prediction_cache()
    if not cache:
        return None

    candidate_keys = list(cache_keys or [])
    if not candidate_keys:
        candidate_keys = build_cache_keys(file_path, original_filename=original_filename)

    for key in candidate_keys:
        result = cache.get(normalize_cache_key(key))
        if isinstance(result, dict):
            return copy.deepcopy(result)

    return None


def write_prediction_cache(cache: dict[str, dict], path: str | None = None) -> str:
    cache_path = resolve_cache_path(path)
    os.makedirs(os.path.dirname(cache_path), exist_ok=True)

    with open(cache_path, "w", encoding="utf-8") as f:
        f.write("# Auto-generated prediction cache for local batch inference.\n")
        f.write("# Generated by /api/v1/analysis/cache/batch.\n\n")
        f.write("PREDICTION_CACHE = ")
        f.write(pformat(cache, sort_dicts=True, width=120))
        f.write("\n")

    return cache_path
